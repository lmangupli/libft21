#include "../includes/libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	str = (char *)malloc(sizeof(char) * (size + 1));
	if (NULL == str)
		return (NULL);
	ft_bzero(str, size + 1);
	return (str);
}
